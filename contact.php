            <div class="panel">
                <div class="heading"><h4>1. How does the blood donation process work?</h4></div>
                <div class="content" style="color:black;">
                
                    <b>Donating blood is a simple thing to do, but can make a big difference in the lives of others. 
                    The donation process from the time you arrive until the time you leave takes about an hour. 
                    The donation itself is only about 8-10 minutes on average. The steps in the process are: </b><br>
        
        <h2>Registration</h2><br>
        <dd>1. You will complete donor registration, which includes information such as your name, address, 
        phone number, and donor identification number (if you have one) . </dd>
        <dd>2. You will be asked to show a donor card, driver’s license or two other forms of ID.</dd>
           <dd>3. Health History and Mini Physical</dd>
           <dd>4. You will answer some questions during a private and confidential interview about your health history and the places you have traveled.</li><br>
           <dd>5. You will have your temperature, hemoglobin, blood pressure and pulse checked.</dd>
           
           <h2>Donation</h2>
         <dd>1. We will cleanse an area on your arm and insert a brand–new, sterile needle for the blood draw. This feels like a quick pinch and is over in seconds.</dd>
          <dd>2. You will have some time to relax while the bag is filling. (For a whole blood donation, it is about 8-10 minutes. If you are donating platelets,
          red cells or plasma by apheresis the collection can take up to 2 hours.)</dd>
           <dd>3. When approximately a pint of blood has been collected, the donation is complete and a staff person will place a bandage on your arm. </dd>
           <br>
           <h2>Refreshments</h2>
           
           <dd>1. You will spend a few minutes enjoying refreshments to allow your body time to adjust to the slight decrease in fluid volume.</dd>
           <dd>2. After 10-15 minutes you can then leave the donation site and continue with your normal daily activities.</dd>
           <dd>3. Enjoy the feeling of accomplishment knowing that you have helped to save lives.</dd>
           <dd>4. Your gift of blood may help up to three people. Donated red blood cells do not last forever. 
           They have a shelf-life of up to 42 days. A healthy donor may donate every 56 days.  </dd>
                
                </div>
                
            </div>
            <div class="panel">
                <div class="heading"><h4>2. What should I do after donating blood?</h4></div>
                <div class="content" style="color:black;">
                    <b>After you give blood:
                    
                        Take the following precautions:

                    </b>
                <br>
                    
                    <dd>1. Drink an extra four glasses (eight ounces each) of non-alcoholic liquids.</dd>
                    <dd>2. Keep your bandage on and dry for the next five hours, and do not do heavy exercising or lifting.</dd>
                    <dd>3. If the needle site starts to bleed, raise your arm straight up and press on the site until the bleeding stops.</dd>
                    <dd>4. Because you could experience dizziness or loss of strength, use caution if you plan to do anything that could put you or others at risk of harm.
                    For any hazardous occupation or hobby,
                    follow applicable safety recommendations regarding your return to these activities following a blood donation.</dd>
                    <dd>5. Eat healthy meals and consider adding iron-rich foods to your regular diet, or discuss taking an iron supplement with your health care provider, 
                        to replace the iron lost with blood donation.</dd>
                    <dd>6. If you get a bruise:  Apply ice to the area intermittently for 10-15 minutes during the first 24 hours. Thereafter, apply warm, moist heat to the 
                          area intermittently for 10-15 minutes. A rainbow of colors may occur for about 10 days.</dd>
                    <dd>7. If you get dizzy or lightheaded:  Stop what you are doing, lie down, and raise your feet until the feeling passes and you feel well enough to safely resume activities.
                         And remember to enjoy the feeling of knowing you have helped save lives!	</dd>
                    <dd>8. Schedule your next appointment. </dd>


                    
                    
                </div>
            </div>
            
            <div class="panel">
                <div class="heading"><h4>3. Will it hurt when you insert the needle?</h4></div>
                <div class="content" style="color:black;">
                    
                 
                        <dd>Only for a moment. Pinch the fleshy, soft underside of your arm. That pinch is similar to what you will feel when the needle is inserted.</dd>
                     
                        
                   
                </div>
            </div>
            
            
              <div class="panel">
                <div class="heading"><h4>4. How long will it take to replenish the pint of blood I donate?</h4></div>
                <div class="content" style="color:black;">
                  
                       <dd>The plasma from your donation is replaced within about 24 hours. Red cells need about four to six weeks for complete replacement.
                       That’s why at least eight weeks are required between whole blood donations.</dd>
                </div>
            </div>
            
            
            
            
              <div class="panel">
                <div class="heading"><h4>5. How often can I donate blood?</h4></div>
                <div class="content" style="color:black;">
                   
                      <dd>You must wait at least eight weeks (56 days) between donations of whole blood and 16 weeks (112 days) between Power Red donations.
                      Platelet apheresis donors may give every 7 days up to 24 times per year. 
                      Regulations are different for those giving blood for themselves (autologous donors).</dd>
                </div>
